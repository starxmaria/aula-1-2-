#Algoritmo média: criara um algoritmo que leia 4 notas e apresente a média final

notal1 = float(input("Digite a primira nota: "))
notal2 = float(input("Digite a segunda nota: "))
notal3 = float(input("Digite a terceira nota: "))
notal4 = float(input("Digite a quarta nota: "))
media = (notal1 + notal2 + notal3 + notal4) / 4
print("A média final obtida: ", media)